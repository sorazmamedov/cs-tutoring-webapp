export function getSlots() {
  let tutors = [
    { id: 11, name: "Jenny Jenssen", slots: [] },
    { id: 12, name: "Andrew Carnegie", slots: [] },
    { id: 13, name: "Victor Zyu", slots: [] },
    { id: 14, name: "Elizabeth Taylor", slots: [] },
    { id: 15, name: "John Doe", slots: [] },
    { id: 16, name: "Arnold Baked", slots: [] },
  ];

  let ampm = ["am", "pm"];
  tutors.forEach((tutor) => {
    let daily = getRand(1, 3);
    for (let i = 0; i < 6; i++) {
      let hours = [];
      for (let j = 0; j < daily; j++) {
        let temp = {};
        temp.slotId = getRand(100, 1000);
        temp.slotDate = `January ${getRand(1, 30)}, 2022`;
        let ap = ampm[getRand(0, 2)];
        let start = ap === "am" ? getRand(8, 12) : getRand(13, 20);
        temp.startHour = `${
          start > 12 && ap === "pm" ? start - 12 : start
        }${ap}`;
        temp.endHour = `${
          start > 12 && ap === "pm" ? start - 11 : start + 1
        }${ap}`;
        temp.booked = getRand(0, 2) ? true : false;

        if (!hours.includes(temp.startHour + temp.endHour)) {
          tutor.slots.push(temp);
          hours.push(temp.startHour + temp.endHour);
        }
      }
    }
  });

  return tutors;
}

